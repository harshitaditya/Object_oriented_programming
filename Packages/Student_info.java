package Packages;

public class Student_info {
    private String name;
    private String course;
    private int age;

    public void setname(String name) {
        this.name = name;

    }

    public String getname() {
        return name;
    }

    public void setage(int age) {
        this.age = age;
    }

    public int getage() {
        return age;
    }

    public void setcourse(String course) {
        this.course = course;
    }

    public String getcourse() {
        return course;
    }
}
